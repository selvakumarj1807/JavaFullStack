class AssignmentDemo {
    public static void main(String[] args) {
        int a;
        
        // Simple assignment
        a = 20;
        System.out.println("After a = 20: " + a);

        // Add and assign
        a += 5;
        System.out.println("After a += 5: " + a);

        // Subtract and assign
        a = 20;
        a -= 3;
        System.out.println("After a -= 3: " + a);

        // Multiply and assign
        a = 20;
        a *= 2;
        System.out.println("After a *= 2: " + a);

        // Divide and assign
        a = 20;
        a /= 4;
        System.out.println("After a /= 4: " + a);
    }
}