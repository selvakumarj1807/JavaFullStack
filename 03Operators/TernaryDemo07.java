class TernaryDemo {
    public static void main(String[] args) {
        int a = 10;
        int b = 20;

        // Using ternary operator
        int max = (a > b) ? a : b;

        System.out.println("Value of a: " + a);
        System.out.println("Value of b: " + b);
        System.out.println("Maximum value: " + max);
    }
}